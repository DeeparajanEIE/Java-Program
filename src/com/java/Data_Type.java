package com.java;

public class Data_Type {


	public static void main(String[] args) {
		byte a=1;
	    int b=10;
	    short c=123;
	    float d=0.1234f;
	    long e=123456;
	    double f=12345678;
	    boolean h=true;
	    System.out.println("Byte a is"+a);
	    System.out.println("Int b is"+b);
	    System.out.println("Short c is"+c);
	    System.out.println("Float d is"+d);
	    System.out.println("Long e is"+e);
	    System.out.println("Double f is"+f);
	    System.out.println("Boolean h is"+h);
	   
	    
	  

	}

}