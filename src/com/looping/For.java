package com.looping;

public class For {

	public static void main(String[] args) {
         for(int i=10; i>5; i--) {
        	int add =i+1;
        	 System.out.println("add:"+add);
         }
         
         for (int i = 1; i < 5; i++) {
        	int sub =i-1;
 			System.out.println("sub:"+sub);
 		}
         
         for (int i = 1; i<= 5; i++) {
        	 int mul=i*2;
 			System.out.println("mul:"+mul);
 		}
	}

}
